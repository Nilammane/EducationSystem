package com.EducationSystem.DTO;

public class CourseStudentDetails {

  private String id;
  private String stud_u_id;
  private String stud_name;
  private int stud_phone;
  private String stud_email;
  private String stud_course;
public String getId() {
	return id;
}
public void setId(String id) {
	this.id = id;
}
public String getStud_u_id() {
	return stud_u_id;
}
public void setStud_u_id(String stud_u_id) {
	this.stud_u_id = stud_u_id;
}
public String getStud_name() {
	return stud_name;
}
public void setStud_name(String stud_name) {
	this.stud_name = stud_name;
}
public int getStud_phone() {
	return stud_phone;
}
public void setStud_phone(int stud_phone) {
	this.stud_phone = stud_phone;
}
public String getStud_email() {
	return stud_email;
}
public void setStud_email(String stud_email) {
	this.stud_email = stud_email;
}
public String getStud_course() {
	return stud_course;
}
public void setStud_course(String stud_course) {
	this.stud_course = stud_course;
}
  
}
